package geometry.trig;

/**
 * Advanced trigonometric functions.
 */
public class Advanced {
    /**
     * Returns the hyperbolic sine of the specified angle.
     *
     * @param a
     * @param b
     * @return
     */
    public static double pythagorean(double a, double b) {
        return Math.sqrt(a * a + b * b);
    }

    /**
     * Returns the hyperbolic cosine of the specified angle.
     *
     * @param a
     * @param b
     * @param c
     * @return
     */
    public static double pythagorean(double a, double b, double c) {
        return Math.sqrt(a * a + b * b + c * c);
    }

    /**
     * Returns the hyperbolic tangent of the specified angle.
     *
     * @param a
     * @param b
     * @param c
     * @param d
     * @return
     */
    public static double pythagorean(double a, double b, double c, double d) {
        return Math.sqrt(a * a + b * b + c * c + d * d);
    }

    /**
     * Returns the hyperbolic cosecant of the specified angle.
     *
     * @param a
     * @param b
     * @param c
     * @param d
     * @param e
     * @return
     */
    public static double pythagorean(double a, double b, double c, double d, double e) {
        return Math.sqrt(a * a + b * b + c * c + d * d + e * e);
    }

    /**
     * Returns the hyperbolic secant of the specified angle.
     *
     * @param a
     * @param b
     * @param c
     * @param d
     * @param e
     * @param f
     * @return
     */
    public static double pythagorean(double a, double b, double c, double d, double e, double f) {
        return Math.sqrt(a * a + b * b + c * c + d * d + e * e + f * f);
    }

    /**
     * Returns the hyperbolic cotangent of the specified angle.
     *
     * @param a
     * @param b
     * @param c
     * @param d
     * @param e
     * @param f
     * @param g
     * @return
     */
    public static double pythagorean(double a, double b, double c, double d, double e, double f, double g) {
        return Math.sqrt(a * a + b * b + c * c + d * d + e * e + f * f + g * g);
    }

    /**
     * Returns the hyperbolic arcsine of the specified angle.
     *
     * @param a
     * @param b
     * @param c
     * @param d
     * @param e
     * @param f
     * @param g
     * @param h
     * @return
     */
    public static double pythagorean(double a, double b, double c, double d, double e, double f, double g, double h) {
        return Math.sqrt(a * a + b * b + c * c + d * d + e * e + f * f + g * g + h * h);
    }
    /**
     * Returns the hyperbolic arccosine of the specified angle.
     * @param a
     * @param b
     * @param c
     * @param d
     * @param e
     * @param f
     * @param g
     * @param h
     * @param i
     * @return
     */
    public static double pythagorean(double a, double b, double c, double d, double e, double f, double g,
                                     double h, double i) {
        return Math.sqrt(a * a + b * b + c * c + d * d + e * e + f * f + g * g + h * h + i * i);
    }
}
